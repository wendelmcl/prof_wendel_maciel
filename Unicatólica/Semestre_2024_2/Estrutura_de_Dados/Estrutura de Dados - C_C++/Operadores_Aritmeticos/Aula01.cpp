#include <iostream>
using namespace std;

int main(){
    int a = 10;
    int b = 2;

    cout << "Soma = " << a + b << endl;
    cout << "Subtração = " << a - b << endl;
    cout << "Multiplicação = " << a * b << endl;
    cout << "Divisão = " << a / b << endl;
    cout << "Módulo = " << a % b << endl;

    return 0;
}