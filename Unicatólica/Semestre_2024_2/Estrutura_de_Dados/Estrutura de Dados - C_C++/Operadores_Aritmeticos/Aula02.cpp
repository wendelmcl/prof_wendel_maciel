#include <iostream>

using namespace std;

int main(){
    int n1, n2;
    cout << "Digite o valor de N1: " << endl;
    cin >> n1;
    cout << "Digite o valor de N2: " << endl;
    cin >> n2;
    cout << "Soma = " << n1 + n2 << endl;
    cout << "Subtração = " << n1 - n2 << endl;
    cout << "Multiplicação = " << n1 * n2 << endl;
    cout << "Divisão = " << n1 / n2 << endl;
    cout << "Módulo = " << n1 % n2 << endl;

    return 0;
}